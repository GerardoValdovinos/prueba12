/**
* \brief    Task 1 - 1ms Period
* \author   Gerardo Valdovinos
* \param    void
* \return   void   
*/
TASK(Task_1ms)
{
    static u16 time = 0;

    /* Call to Control of pendubot function */
    vfnCtrl_Pendubot();
    
    time++;
    if(time >= 400)
    {
        PORTA_PA0 ^= 1; 
        time = 0;
    } 
    
    (void)Os_TerminateTask();     
}
